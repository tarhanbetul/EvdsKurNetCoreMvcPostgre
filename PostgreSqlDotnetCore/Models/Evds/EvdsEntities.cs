        protected override void OnConfiguring(DbContextOptionsBuilder optionsBuilder)
        {
            if (!optionsBuilder.IsConfigured)
            {
                IConfigurationRoot configuration = new ConfigurationBuilder()
               .SetBasePath(AppDomain.CurrentDomain.BaseDirectory)
               .AddJsonFile("appsettings.json")
               .Build();
                string POSTGRES_URI = Environment.GetEnvironmentVariable("POSTGRES_URI");
                string postgres_str = configuration.GetConnectionString("DefaultConnection");
                Console.WriteLine(postgres_str);
                if (postgres_str.Length > 0)
                {
                    optionsBuilder.UseNpgsql(configuration.GetConnectionString("DefaultConnection"), options =>
                    {
                        options.SetPostgresVersion(9,6);
                    });
                    base.OnConfiguring(optionsBuilder);
                }
                else
                {
                    optionsBuilder.UseNpgsql(POSTGRES_URI, options =>
                    {
                        options.SetPostgresVersion(9,6);
                    });   
                }
            }
        }

